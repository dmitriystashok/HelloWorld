public class Solution {
    public static void main(String[] args)
    {
        print5Times("JavaRush. Learn once - use anywhere");
        print5Times("JavaRush. Learn once - use anywhere");
    }


    public static void print5Times(String s)
    {
        for (int i = 0; i < 5; i++) {
            System.out.println(s);

        }
    }
}
